            <p class="mb-3 faq-question">{{ $index + 1 }} - How to Ask for Help ?</p>
            <blockquote>
                <ol>
                    <li> Do not contact members by private message, the questions you have and the answers given can benefit everyone and you will be more likely to have a quick answer.</li>
                    <li> If you have a problem or question, make sure to do a minimum of research before. This server is dedicated to mutual aid, but our role is not to do Google searches for you!</li>
                    <li> Do not ask for solutions.</li>
                    <li> Ask for help in the <span class="text-decoration-underline">#Help › Text</span> category and no where else. (<em>Otherwise the question will be deleted</em>)</li>
                    <li> When you ask a question in the <span class="text-decoration-underline">#Help › Text</span>, include as many things as possible so that we can help you :</li>
                        <ul>
                            <li>- Explain the error message (or lack of errors!) You get.</li>
                            <li>- Put a screenshot of the problem.</li>
                            <li>- Tell us your code or the problematic lines.</li>
                        </ul>
                    <li> If you want to share code, share it directly (check <a href="/pages/faq#post-code">here</a> to know how), don't upload a file. If the code is too long or there are too many files, give us a link to a GitHub/GitLab/Bitbucket repository or Github Gists ...</li>
                    <li> Members who can/want to help regularly visit the Help rooms, so when you ask for help, <strong>DO NOT mention someone or everyone</strong> to ask for help personally (unless you've already interacted with that same person. on the same problem to warn him).</li>
                    <li> Do Not Post the same question in multiple channels.</li>
                    <li> You should chat using <span class="text-decoration-underline">#help-chat</span> when you are connected to the <span class="text-decoration-underline">#Help › Voice</span>.</li>
                    <li> Please, feel free to share the solution in case you found on your own.</li>
                    <li> Do Not Ask for help in exchange for money or Ask for money in exchange for help.</li>
                    <li> <a href="https://dontasktoask.com/" target="_blank">https://dontasktoask.com/</a></li>
                </ol>
            </blockquote>
